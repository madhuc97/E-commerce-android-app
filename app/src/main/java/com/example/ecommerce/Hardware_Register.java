package com.example.ecommerce;

public class Hardware_Register {
    private String hardwareName;
    private String hardwarePrice;
    private String hardwareStock;
    private int hardwareImage;
    private String hardwareQuantity;


    public String getHardwareName() {
        return hardwareName;
    }

    public void setHardwareName(String hardwareName) {
        this.hardwareName = hardwareName;
    }

    public String getHardwarePrice() {
        return hardwarePrice;
    }

    public void setHardwarePrice(String hardwarePrice) {
        this.hardwarePrice = hardwarePrice;
    }

    public String getHardwareStock() {
        return hardwareStock;
    }

    public void setHardwareStock(String hardwareStock) {
        this.hardwareStock = hardwareStock;
    }

    public int getHardwareImage() {
        return hardwareImage;
    }

    public void setHardwareImage(int hardwareImage) {
        this.hardwareImage = hardwareImage;
    }

    public String getHardwareQuantity() {
        return hardwareQuantity;
    }

    public void setHardwareQuantity(String hardwareQuantity) {
        this.hardwareQuantity = hardwareQuantity;
    }

    public Hardware_Register() {
    }
}

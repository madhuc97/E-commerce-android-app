package com.example.ecommerce;

public class Electrical_Register {

    private String electricalName;
    private String electricalPrice;
    private String electricalStock;
    private int electricalImage;
    private String electricalQuantity;

    public String getElectricalName() {
        return electricalName;
    }

    public void setElectricalName(String electricalName) {
        this.electricalName = electricalName;
    }

    public String getElectricalPrice() {
        return electricalPrice;
    }

    public void setElectricalPrice(String electricalPrice) {
        this.electricalPrice = electricalPrice;
    }

    public String getElectricalStock() {
        return electricalStock;
    }

    public void setElectricalStock(String electricalStock) {
        this.electricalStock = electricalStock;
    }

    public int getElectricalImage() {
        return electricalImage;
    }

    public void setElectricalImage(int electricalImage) {
        this.electricalImage = electricalImage;
    }

    public String getElectricalQuantity() {
        return electricalQuantity;
    }

    public void setElectricalQuantity(String electricalQuantity) {
        this.electricalQuantity = electricalQuantity;
    }

    public Electrical_Register() {
    }
}

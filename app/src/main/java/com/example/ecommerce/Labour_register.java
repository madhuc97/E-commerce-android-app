package com.example.ecommerce;

public class Labour_register {

    private String labour_Name;
    private String labour_Phoneno;
    private String labour_Description;
    private String labour_expertise;
    private String labour_experiance;
    private String labour_price;
    private String labour_servicetype;

    public String getLabour_Name() {
        return labour_Name;
    }

    public void setLabour_Name(String labour_Name) {
        this.labour_Name = labour_Name;
    }

    public String getLabour_Phoneno() {
        return labour_Phoneno;
    }

    public void setLabour_Phoneno(String labour_Phoneno) {
        this.labour_Phoneno = labour_Phoneno;
    }

    public String getLabour_Description() {
        return labour_Description;
    }

    public void setLabour_Description(String labour_Description) {
        this.labour_Description = labour_Description;
    }

    public String getLabour_expertise() {
        return labour_expertise;
    }

    public void setLabour_expertise(String labour_expertise) {
        this.labour_expertise = labour_expertise;
    }

    public String getLabour_experiance() {
        return labour_experiance;
    }

    public void setLabour_experiance(String labour_experiance) {
        this.labour_experiance = labour_experiance;
    }

    public String getLabour_price() {
        return labour_price;
    }

    public void setLabour_price(String labour_price) {
        this.labour_price = labour_price;
    }

    public String getLabour_servicetype() {
        return labour_servicetype;
    }

    public void setLabour_servicetype(String labour_servicetype) {
        this.labour_servicetype = labour_servicetype;
    }
}
